public class No01_DanHangDemo {
	public static void main(String[] args) {
		int i = 5;
		i = i + 1;
		System.out.println("i = i + 5의 값 :" + i);
		
		int j = 5;
		j++;
		System.out.println("j++의 값 :" + j);
		
		int k = 5;
		++k;
		System.out.println("++k의 값 :" + k);
		
		
	}
}

